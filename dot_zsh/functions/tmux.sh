alias tm='tmux'

# new session-name
alias tm-new='tmux new -A -s'
alias tmn='tmux new -A -s'

# join session-name
# note - this will detach others connected to the current session
alias tm-join='tmux attach -d -t'
alias tmj='tmux attach -d -t'

# ls
